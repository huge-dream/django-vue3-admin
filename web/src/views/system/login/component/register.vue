<template>
  <el-form ref="formRef" size="large" class="login-content-form" :model="state.ruleForm" :rules="rules"
           @keyup.enter="RegisterClick">
    <el-form-item class="login-animation1" prop="username">
      <el-input type="text" :placeholder="$t('message.register.registerPlaceholder1')" v-model="ruleForm.username"
                clearable autocomplete="off">
        <template #prefix>
          <el-icon class="el-input__icon">
            <ele-User/>
          </el-icon>
        </template>
      </el-input>
    </el-form-item>
    <el-form-item class="login-animation2" prop="password">
      <el-input :type="isShowPassword ? 'text' : 'password'" :placeholder="$t('message.register.registerPlaceholder2')"
                v-model="ruleForm.password">
        <template #prefix>
          <el-icon class="el-input__icon">
            <ele-Unlock/>
          </el-icon>
        </template>
        <template #suffix>
          <i class="iconfont el-input__icon login-content-password"
             :class="isShowPassword ? 'icon-yincangmima' : 'icon-xianshimima'"
             @click="isShowPassword = !isShowPassword">
          </i>
        </template>
      </el-input>
    </el-form-item>

    <el-form-item class="login-animation2" prop="password2">
      <el-input :type="isShowPassword ? 'text' : 'password'" :placeholder="$t('message.register.registerPlaceholder3')"
                v-model="ruleForm.password2">
        <template #prefix>
          <el-icon class="el-input__icon">
            <ele-Unlock/>
          </el-icon>
        </template>
        <template #suffix>
          <i class="iconfont el-input__icon login-content-password"
             :class="isShowPassword ? 'icon-yincangmima' : 'icon-xianshimima'"
             @click="isShowPassword = !isShowPassword">
          </i>
        </template>
      </el-input>
    </el-form-item>

    <!--    name(昵称)-->
    <el-form-item class="login-animation2" prop="name">
      <el-input type="text" :placeholder="$t('message.register.registerPlaceholder4')" v-model="ruleForm.name"
                clearable autocomplete="off">
        <template #prefix>
          <el-icon class="el-input__icon">
            <ele-Avatar/>
          </el-icon>
        </template>
      </el-input>
    </el-form-item>
    <!--    email(邮箱)-->
    <el-form-item class="login-animation2" prop="email">
      <el-input type="text" :placeholder="$t('message.register.registerPlaceholder5')" v-model="ruleForm.email"
                clearable autocomplete="off">
        <template #prefix>
          <el-icon class="el-input__icon">
            <ele-Message/>
          </el-icon>
        </template>
      </el-input>
    </el-form-item>

    <el-form-item class="login-animation3" v-if="isShowCaptcha" prop="captcha">
      <el-col :span="15">
        <el-input type="text" maxlength="4" :placeholder="$t('message.register.registerPlaceholder6')"
                  v-model="ruleForm.captcha" clearable autocomplete="off">
          <template #prefix>
            <el-icon class="el-input__icon">
              <ele-Position/>
            </el-icon>
          </template>
        </el-input>
      </el-col>
      <el-col :span="1"></el-col>
      <el-col :span="8">
        <el-button class="login-content-captcha">
          <el-image :src="ruleForm.captchaImgBase" @click="refreshCaptcha"/>
        </el-button>
      </el-col>
    </el-form-item>
    <el-form-item class="login-animation4">
      <el-button type="primary" class="login-content-submit" round @click="RegisterClick"
                 :loading="loading.signIn">
        <span>{{ $t('message.register.registerBtnText') }}</span>
      </el-button>
    </el-form-item>
  </el-form>
</template>

<script lang="ts">
import {computed, defineComponent, onMounted, reactive, ref, toRefs} from 'vue';
import {useRoute, useRouter} from 'vue-router';
import {ElMessage, FormRules} from 'element-plus';
import {useI18n} from 'vue-i18n';
import {storeToRefs} from 'pinia';
import {useThemeConfig} from '/@/stores/themeConfig';
import {formatAxis} from '/@/utils/formatTime';
import * as loginApi from '/@/views/system/login/api';
import {SystemConfigStore} from '/@/stores/systemConfig';
import {errorMessage, successMessage} from '/@/utils/message';
import {DictionaryStore} from "/@/stores/dictionary";
import {NextLoading} from "/@/utils/loading";
import {useUserInfo} from "/@/stores/userInfo";
import {Md5} from "ts-md5";
import {Session} from "/@/utils/storage";
import Cookies from "js-cookie";
import {initFrontEndControlRoutes} from "/@/router/frontEnd";
import {initBackEndControlRoutes} from "/@/router/backEnd";
import {verifyEmail} from "/@/utils/toolsValidate";

export default defineComponent({
  name: 'RegisterAccount',
  setup() {
    const {t} = useI18n();
    const storesThemeConfig = useThemeConfig();
    const {themeConfig} = storeToRefs(storesThemeConfig);
    const route = useRoute();
    const router = useRouter();
    const state = reactive({
      isShowPassword: false,
      ruleForm: {
        username: '',
        password: '',
        password2: '',
        name: '',
        email: '',
        captcha: '',
        captchaKey: '',
        captchaImgBase: '',
      },
      loading: {
        signIn: false,
      },
    });
    const rules = reactive<FormRules>({
      username: [
        {required: true, message: '请填写账号', trigger: 'blur'},
      ],
      password: [
        {
          required: true,
          message: '请填写密码',
          trigger: 'blur',
        },
        {
          validator: (rule, value, callback) => {
            const minLength = 8;
            const hasUpperCase = /[A-Z]/.test(value);
            const hasLowerCase = /[a-z]/.test(value);
            const hasDigit = /\d/.test(value);
            const validCharacters = /^[A-Za-z\d]+$/.test(value);

            if (value.length < minLength) {
              callback(new Error('密码长度必须大于等于八位'));
            } else if (!hasUpperCase) {
              callback(new Error('密码必须至少包含一个大写字母'));
            } else if (!hasLowerCase) {
              callback(new Error('密码必须至少包含一个小写字母'));
            } else if (!hasDigit) {
              callback(new Error('密码必须至少包含一个数字'));
            } else if (!validCharacters) {
              callback(new Error('密码只能包含大小写字母和数字'));
            } else {
              callback();
            }
          },
          trigger: 'blur',
        }
      ],
      password2: [
        {
          required: true,
          message: '请填写确认密码',
          trigger: 'blur',
        },
        {
          validator: (rule, value, callback) => {
            if (value !== state.ruleForm.password) {
              callback(new Error('两次输入的密码不一致'));
            } else {
              callback();
            }
          },
          trigger: 'blur'
        }
      ],
      name: [
        {
          required: true,
          message: '请填写昵称',
          trigger: 'blur',
        },
      ],
      email: [
        {
          required: true,
          message: '请填写邮箱',
          trigger: 'blur',
        },
        {
          validator: (rule, value, callback) => {
            if (!verifyEmail(value)) {
              callback(new Error('请输入有效的邮箱地址'));
            } else {
              callback();
            }
          },
          trigger: 'blur',
        }
      ],
      captcha: [
        {
          required: true,
          message: '请填写验证码',
          trigger: 'blur',
        },
      ],
    })
    const formRef = ref();
    // 时间获取
    const currentTime = computed(() => {
      return formatAxis(new Date());
    });
    // 是否关闭验证码
    const isShowCaptcha = computed(() => {
      return SystemConfigStore().systemConfig['register.register_captcha_state'];
    });

    const getCaptcha = async () => {
      loginApi.getCaptcha().then((ret: any) => {
        state.ruleForm.captchaImgBase = ret.data.image_base;
        state.ruleForm.captchaKey = ret.data.key;
      });
    };
    const refreshCaptcha = async () => {
      state.ruleForm.captcha = ''
      loginApi.getCaptcha().then((ret: any) => {
        state.ruleForm.captchaImgBase = ret.data.image_base;
        state.ruleForm.captchaKey = ret.data.key;
      });
    };
    const RegisterClick = async () => {
      if (!formRef.value) return
      await formRef.value.validate((valid: any) => {
        if (valid) {
          loginApi.register({
            ...state.ruleForm,
            password: Md5.hashStr(state.ruleForm.password),
            password2: Md5.hashStr(state.ruleForm.password2)
          }).then((res: any) => {
            if (res.code === 2000) {
              Session.set('token', res.data.access);
              Cookies.set('username', res.data.name);
              if (!themeConfig.value.isRequestRoutes) {
                // 前端控制路由，2、请注意执行顺序
                initFrontEndControlRoutes();
                loginSuccess();
              } else {
                // 模拟后端控制路由，isRequestRoutes 为 true，则开启后端控制路由
                // 添加完动态路由，再进行 router 跳转，否则可能报错 No match found for location with path "/"
                initBackEndControlRoutes();
                // 执行完 initBackEndControlRoutes，再执行 signInSuccess
                loginSuccess();
              }
            }
          }).catch((err: any) => {
            // 登录错误之后，刷新验证码
            refreshCaptcha();
          });
        } else {
          errorMessage("请填写注册信息")
        }
      });
    };

    const getUserInfo = () => {
      useUserInfo().setUserInfos();
    };

    // 登录成功后的跳转
    const loginSuccess = () => {
      //登录成功获取用户信息,获取系统字典数据
      getUserInfo();
      //获取所有字典
      DictionaryStore().getSystemDictionarys();

      // 初始化登录成功时间问候语
      let currentTimeInfo = currentTime.value;
      // 登录成功，跳到转首页
      // 如果是复制粘贴的路径，非首页/登录页，那么登录成功后重定向到对应的路径中
      if (route.query?.redirect) {
        router.push({
          path: <string>route.query?.redirect,
          query: Object.keys(<string>route.query?.params).length > 0 ? JSON.parse(<string>route.query?.params) : '',
        });
      } else {
        router.push('/');
      }
      // 登录成功提示
      // 关闭 loading
      state.loading.signIn = true;
      const signInText = t('message.signInText');
      ElMessage.success(`${currentTimeInfo}，${signInText}`);
      // 添加 loading，防止第一次进入界面时出现短暂空白
      NextLoading.start();
    };
    onMounted(() => {
      getCaptcha();
      //获取系统配置
      SystemConfigStore().getSystemConfigs();
    });


    return {
      refreshCaptcha,
      RegisterClick,
      isShowCaptcha,
      state,
      formRef,
      rules,
      ...toRefs(state),
    };
  },
});
</script>

